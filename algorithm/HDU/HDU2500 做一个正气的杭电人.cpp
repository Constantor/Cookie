#include <iostream>
#include <stdio.h>
using namespace std;

int main(int argc, char *argv[])
{
	int t,n;
	cin>>t;
	while(t--)
	{
	    cin>>n;
	    for(int i=0;i<n*3;i++)
	    {
	        for(int j=0;j<n;j++)
	        {
	            printf("HDU");
	        }
	        printf("\n");
	    }
	}
	return 0;
}
